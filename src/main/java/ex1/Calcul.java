package ex1;

/**
 * Class used to make any calculs
 */
public class Calcul {

	/**
	 * Method used to add two numbers 
	 * @param entier1
	 * @param entier2
	 * @return result of calcul
	 */
	public int additionnerDeuxNombres(int entier1, int entier2) {
		return entier1 + entier2;
	}

	/**
	 * Method used to soustract two numbers
	 * @param entier1
	 * @param entier2
	 * @return result of calcul
	 */
	public int soustraireDeuxNombres(int entier1, int entier2) {
		return entier1 - entier2;
	}
}
